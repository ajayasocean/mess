# keys() method
a1 = {'iron-man': 'Tony', 'CA': 'Steve', 'BW': 'Natasha', 'hulk': 'Banner'}
keyList = a1.keys()
print('Keys: ', keyList, '\n', type(keyList))
# value() method
valueList = a1.values()
print('Values: ', valueList, '\n', type(valueList))

itemList = a1.items()
print('items: ', itemList, '\n', type(itemList))
