# control statements
# if statement
greeting = 'Good Morning'
if greeting == 'Morning':
    print(greeting)

# if..else statement
if greeting == 'Morning':
    print('Not same')
else:
    print('else effect', greeting)

# if..elif..else statement
if greeting == 'Morning':
    print('Not same')
elif greeting == 'Good Morning':
    print(greeting)
else:
    print('anything')
