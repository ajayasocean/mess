# Program to do arithmatic operation using user input.
print('This program take input from user and performs arithmatic operations on them\n')
# accepting input from user.
a = eval(input('Please enter first  number for operation\n'))
b = eval(input('Please enter second  number for operation\n'))
print('exponential result', a**b)
print("multiplication result", a*b)
print("division result", a/b)
print("floor division result", a//b)
print("Modulus result", a % b)
print('addition result', a+b)
print('subtraction result', a-b)
